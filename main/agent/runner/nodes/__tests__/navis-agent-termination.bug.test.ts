/**
 * Bug Condition Exploration Test — Navis Agent Termination Fix
 *
 * **Validates: Requirements 1.1, 1.2, 1.3, 1.4, 1.5**
 *
 * Property 1: Bug Condition — Navis Completion Termination
 *
 * CRITICAL: These tests MUST FAIL on unfixed code — failure confirms the bug exists.
 * DO NOT attempt to fix the code or the tests when they fail.
 *
 * Bug: When Navis completes and returns results to web_explorer after Phase 2 (deep investigation),
 * the agent continues running instead of terminating cleanly. The graph fails to route to END,
 * instead looping back to brain or web_explorer, causing indefinite execution or duplicate outputs.
 *
 * Root cause: The graph's conditional edges do not properly handle the `webExplorerComplete` flag
 * when Navis finishes. The brain's completion signal generation may fail or return null, leaving
 * the graph without a clear routing path to END.
 *
 * Expected counterexamples (unfixed code):
 *   - Graph routes back to brain instead of END when web_explorer completes
 *   - Graph routes back to web_explorer instead of END
 *   - completionSignal is null or invalid
 *   - routingDecision is null or doesn't route to END
 *   - Agent loops indefinitely or produces duplicate outputs
 */

